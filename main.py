class MultiTreatmentModal(Modal):
    def __init__(self, equipments):
        super().__init__(title="‡∏•‡∏á Treatment ‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤‡∏´‡∏ô‡∏∂‡πà‡∏á‡∏Ñ‡∏ô")
